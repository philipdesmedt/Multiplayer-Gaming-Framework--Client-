package be.tbs.mgf.exceptions;

public class LeaveException extends FrameworkException {

	/**
	 * Serial Version UID
	 */
	private static final long serialVersionUID = 1493251934326077209L;

	/**
	 * Construct a new Leave Error with given message
	 * @param message - the reason a leave operation failed
	 */
	public LeaveException(String message) {
		super(message);
	}
	
	/**
	 * Construct a new Leave Error with given message and cause
	 * @param message - the reason a leave operation failed
	 * @param cause - the underlying cause the operation failed
	 */
	public LeaveException(String message, Throwable cause) {
		super(message, cause);
	}

}
